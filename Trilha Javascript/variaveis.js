/**
 * #Variáveis
 * 
 * Nomes simbólicos para receber algum valor 
 * Atalhos de código
 * Identificadores
 * Fracamente tipada 
 * 3 palavras reservadas para criar uma variavel 
 
    * var
    * let
    * const
 */

//var
var clima = "quente" // valor pode ser alterado e do tipo string
clima = "frio"

console.log(clima)
console.log(typeof clima)

// let (forma moderna de declarar uma variavel sempre preferivel utiliza-la)
let oClima = "quente"
oClima = "frio"

console.log(oClima)

let aTemperatura = true // boolean
console.log(typeof aTemperatura)

//const 
const temperatura = "quente"
temperatura = "frio"

console.log(temperatura) // constante não é possivel alterar valor

